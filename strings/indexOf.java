package strings;
public class indexOf {
    public static void main(String[] args) {
        String s = "Himanshu Gupta";
        System.out.println(s.indexOf('u')); //phela u 
        System.out.println(s.lastIndexOf('u')); //last u 
        System.out.println(s.indexOf('z')); // non existence
    }
}
